"""
Operadores Aritméticos
+, -, *, /, //, **, %, ()

"""

print(10 + 10)  # Também serve para concatenação (juntar strings)
print(10 - 5)
print(10 * 2)  # Também serve como repetição de strings
print(10 / 2)
print(10 // 3)  # Divisão arredondada
print(10 ** 2)  # Potenciação
print(10 % 3)
print(10 * (1+1))
